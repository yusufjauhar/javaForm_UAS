    private void jButtonSEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSEARCHActionPerformed
        // TODO add your handling code here:
        String searchName = jTextFieldSEARCH.getText();

    // Inisialisasi koneksi ke database
    String url = "jdbc:mysql://localhost/uas_oop"; // Ganti 'nama_database' dengan nama database Anda
    String user = "username_database"; // Ganti 'username_database' dengan username database Anda
    String password = "password_database"; // Ganti 'password_database' dengan password database Anda

    try {
        // Buat koneksi ke database
        Connection connection = DriverManager.getConnection(url, user, password);

        // Query untuk mencari data berdasarkan nama
        String query = "SELECT * FROM data_ktp WHERE nama LIKE ?";

        // Buat prepared statement
        PreparedStatement statement = connection.prepareStatement(query);

        // Set parameter pada statement sesuai kriteria pencarian nama
        statement.setString(1, "%" + searchName + "%");

        // Jalankan perintah untuk melakukan pencarian
        ResultSet resultSet = statement.executeQuery();

        // Cek apakah ada hasil yang ditemukan
        if (resultSet.next()) {
            // Jika data ditemukan, tampilkan data pada input field
            jTextFieldNAMA.setText(resultSet.getString("nama"));
            jTextFieldTEMPATTGLLAHIR.setText(resultSet.getString("tempat_tanggal_lahir"));
            jTextFieldALAMAT.setText(resultSet.getString("alamat"));
            jTextFieldAGAMA.setText(resultSet.getString("agama"));
            jTextFieldPEKERJAAN.setText(resultSet.getString("pekerjaan"));
            jTextFieldBERLAKUHINGGA.setText(resultSet.getString("berlaku_hingga"));
            jTextFieldKEWARGANEGARAAN.setText(resultSet.getString("kewarganegaraan"));
            jTextFieldNIK.setText(resultSet.getString("nik"));

            // Tentukan jenis kelamin berdasarkan nilai di database
            String jenisKelamin = resultSet.getString("jenis_kelamin");
            if ("Laki-laki".equals(jenisKelamin)) {
                jRadioButtonLAKILAKI.setSelected(true);
                jRadioButtonPEREMPUAN.setSelected(false);
            } else if ("Perempuan".equals(jenisKelamin)) {
                jRadioButtonLAKILAKI.setSelected(false);
                jRadioButtonPEREMPUAN.setSelected(true);
            }

        } else {
            // Jika data tidak ditemukan, kosongkan semua input field dan unselect jenis kelamin
            jTextFieldNAMA.setText("");
            jTextFieldTEMPATTGLLAHIR.setText("");
            jTextFieldALAMAT.setText("");
            jTextFieldAGAMA.setText("");
            jTextFieldPEKERJAAN.setText("");
            jTextFieldBERLAKUHINGGA.setText("");
            jTextFieldKEWARGANEGARAAN.setText("");
            jTextFieldNIK.setText("");

            jRadioButtonLAKILAKI.setSelected(false);
            jRadioButtonPEREMPUAN.setSelected(false);

            // Tampilkan pesan bahwa data dengan nama tersebut tidak ditemukan
            JOptionPane.showMessageDialog(this, "Data dengan nama tersebut tidak ditemukan!");
        }

        // Tutup koneksi, statement, dan resultSet
        resultSet.close();
        statement.close();
        connection.close();

    } catch (SQLException e) {
        // Tangani kesalahan apabila terjadi kesalahan saat melakukan pencarian data di database
        JOptionPane.showMessageDialog(this, "Error saat mencari data di database: " + e.getMessage());
    }
    }//GEN-LAST:event_jButtonSEARCHActionPerformed

