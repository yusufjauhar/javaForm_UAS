    private void jButtonSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSIMPANActionPerformed
        // TODO add your handling code here:
          // Ambil nilai-nilai dari input field
    String nama = jTextFieldNAMA.getText();
    String tempatTanggalLahir = jTextFieldTEMPATTGLLAHIR.getText();
    String alamat = jTextFieldALAMAT.getText();
    String agama = jTextFieldAGAMA.getText();
    String pekerjaan = jTextFieldPEKERJAAN.getText();
    String berlakuHingga = jTextFieldBERLAKUHINGGA.getText();
    String kewarganegaraan = jTextFieldKEWARGANEGARAAN.getText();
    String nik = jTextFieldNIK.getText(); // Ambil NIK dari input field

    // Tentukan jenis kelamin yang dipilih
    String jenisKelamin = "";
    if (jRadioButtonLAKILAKI.isSelected()) {
        jenisKelamin = "Laki-laki";
    } else if (jRadioButtonPEREMPUAN.isSelected()) {
        jenisKelamin = "Perempuan";
    }

    // Inisialisasi koneksi ke database
    String url = "jdbc:mysql://localhost/uas_oop"; 
    String user = "username_database"; 
    String password = "password_database"; 

    try {
        // Buat koneksi ke database
        Connection connection = DriverManager.getConnection(url, user, password);

        // Query untuk menyimpan data ke tabel yang sesuai dalam database
        String query = "INSERT INTO data_ktp (nama, tempat_tanggal_lahir, jenis_kelamin, alamat, agama, pekerjaan, berlaku_hingga, kewarganegaraan, nik) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

        // Buat prepared statement
        PreparedStatement statement = connection.prepareStatement(query);

        // Set parameter pada statement sesuai data yang akan disimpan
        statement.setString(1, nama);
        statement.setString(2, tempatTanggalLahir);
        statement.setString(3, jenisKelamin);
        statement.setString(4, alamat);
        statement.setString(5, agama);
        statement.setString(6, pekerjaan);
        statement.setString(7, berlakuHingga);
        statement.setString(8, kewarganegaraan);
        statement.setString(9, nik);

        // Jalankan perintah untuk menyimpan data
        statement.executeUpdate();

        // Tampilkan pesan keberhasilan kepada pengguna
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database!");

        // Tutup koneksi dan statement
        statement.close();
        connection.close();

    } catch (SQLException e) {
        // Tangani kesalahan apabila terjadi kesalahan saat menyimpan data ke database
        JOptionPane.showMessageDialog(this, "Error saat menyimpan data ke database: " + e.getMessage());
    }
    }//GEN-LAST:event_jButtonSIMPANActionPerformed

